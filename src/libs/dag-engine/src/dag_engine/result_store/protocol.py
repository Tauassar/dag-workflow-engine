import typing as t

class ResultStore(t.Protocol):
    async def get_key(self, workflow_id: str, node_id: str) -> str: ...
    async def save_result(self, workflow_id: str, node_id: str, attempt: int, result: t.Any, ttl_seconds: int | None = None) -> None: ...
    async def get_result(self, workflow_id: str, node_id: str) -> dict[str, t.Any] | None: ...
    async def list_results(self, workflow_id: str) -> dict[str, dict[str, t.Any]]: ...
    async def delete_results(self, workflow_id: str) -> None: ...
